using System.Windows;

namespace TOGIRRO_ControlTesting
{
    //========================================================================================================================================
    //===Окно вывода ошибки SQL Server========================================================================================================
    //========================================================================================================================================
    public partial class SQLErrorWindow : Window
    {
        //------------------------------------------------------------------------------------------------------------------------------------
        //---Конструктор и вспомогательные методы---------------------------------------------------------------------------------------------
        //------------------------------------------------------------------------------------------------------------------------------------
        #region
        /*
            Конструктор, инициализация параметров
        */
        public SQLErrorWindow()
        {
            InitializeComponent();
        }

        /*
            Событие закрытия окна
        */
        private void Window_Closing(object sender, System.ComponentModel.CancelEventArgs e)
        {
            Workfield.WorkWindow.IsEnabled = true;
            if (Workfield.isFatalError)
            {
                Workfield.WorkWindow.Close();
            }
        }
        #endregion
        //------------------------------------------------------------------------------------------------------------------------------------
    }
    //========================================================================================================================================
}
